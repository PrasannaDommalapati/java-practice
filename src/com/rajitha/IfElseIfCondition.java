package com.rajitha;

public class IfElseIfCondition {
    public static void main(String args[]){
        int i=900;
        int j=300;

        if(i==j){

            System.out.println("This is True");

        }else if(j<i){

            System.out.println("This is false");

        }else {

            System.out.println("None of the above statements are correct");
        }
    }
}
